// Initialize user statistics from localStorage or create a new object if not present
let userStatistics = JSON.parse(localStorage.getItem("WordUserStats")) || {
  totalWordsFound: 0,
  totalGamesPlayed: 0,
  finalAttemptsPerGame: [], // Initialize an empty array to store final attempts for each game
  userCoin: 0, // Initialize the userCoin property with 0 coins
};

const saveUserStatistics = () => {
  // Save user statistics to localStorage as a JSON string
  localStorage.setItem("WordUserStats", JSON.stringify(userStatistics));
};

const showUserStatistics = () => {
  const resultsElement = document.getElementById("gameEnd");
  const statisticsString = `TOTAL DE PALABRAS ENCONTRADAS ${userStatistics.totalWordsFound}\n`;

  resultsElement.textContent = statisticsString;
  const attemptsPerWordDisplay = document.getElementById(
    "attemptsPerWordDisplay"
  );
  attemptsPerWordDisplay.innerHTML = "";

  const attemptsCountMap = userStatistics.finalAttemptsPerGame.reduce(
    (map, attempts) => {
      map[attempts] = (map[attempts] || 0) + 1;
      return map;
    },
    {}
  );

  const attemptsTextList = Object.entries(attemptsCountMap).map(
    ([attempts, count]) => {
      return `Encontré ${count} palabras en ${attempts}. suposición.\n`;
    }
  );

  const gameDiv = document.createElement("div");
  gameDiv.classList.add("pt-5");
  gameDiv.textContent = attemptsTextList.join("\n");
  attemptsPerWordDisplay.appendChild(gameDiv);
  var box = document.getElementById("statss");
  box.style.display = "none";

  // Calculate the average play time in seconds
  const averagePlayTime = localStorage.getItem('averagePlayTime');
  if (averagePlayTime !== null) {
    const averagePlayTimeInSeconds = Math.round(parseFloat(averagePlayTime));
    const averagePlayTimeDisplay = document.getElementById('averagePlayTimeDisplay');
    averagePlayTimeDisplay.textContent = `Tiempo de juego: ${averagePlayTimeInSeconds} segundos.`;
  }


  // Create a reload button
  const reloadButton = document.createElement("button");
  reloadButton.textContent = "NUEVO JUEGO";
  reloadButton.classList.add("wordButton");
  reloadButton.addEventListener("click", () => {
    location.reload();
  });

  // Append the reload button to the resultsElement
  resultsElement.appendChild(reloadButton);
  var box = document.getElementById("endContainer");
  box.style.display = "block";
  var box1 = document.getElementById("attemptsPerWordDisplay");
  box1.style.display = "block";
  var box2 = document.getElementById("gameEnd");
  box2.style.display = "block";
  var box2 = document.getElementById("infoo");
  box2.style.display = "none";

  //
};

